package javaPractice;

public class Lec2IncrementOperator {

	public static void main(String[] args) {
		
		int a=10;
		a++;  //a=a+1;
		
		System.out.println(a);
		
		//add 2 increment;
			int c=15;
			c+=2;
			
			System.out.println(c);
		
			a+=2;
			//increment of 3; 
			int b=10;
			
			b+=3; //b=b+3;
			
			System.out.println(b);
			
			//decrement by 1; n=n-1
			int n =10;
			n--;
			System.out.println(n);
			
			//decrement by 3; m=m-3
			
			int m=10;
			m-=3;
			System.out.println(m);
			

	}

}
